public class Perro {
    String nombre;
    String raza;
    String color;
    int edad;
    boolean vacunasAlDia;

    public Perro(String nombre, String raza, String color, int edad, boolean vacunasAlDia) {
        this.nombre = nombre;
        this.raza = raza;
        this.color = color;
        this.edad = edad;
        this.vacunasAlDia = vacunasAlDia;
    }
}
